# 1. Постановка задачи

### 1.1 Бизнес-постановка задачи
# Разработать систему прогнозирования выживаемости пассажиров при кораблекрушении на примере «Титаника». 
# Это позволит лучше понять ключевые факторы риска (пол, возраст, социальный класс, наличие родственников), а также применить полученные идеи для разработки современных стратегий безопасности морских перевозок, страхования и эвакуационных протоколов.

### 1.2 Постановка ML-задачи
# Задача бинарной классификации: по имеющимся признакам пассажира предсказать целевую переменную Survived (1 — пассажир выжил, 0 — пассажир погиб).

