# Целевая переменная: Survived (бинарная)


# 2. Выбор и обоснование метрики качества

# В качестве основной метрики выбрана ROC-AUC. Дополнительно можно использовать F1-score.

# Обоснование: 
# Датасет характеризуется умеренным дисбалансом классов: выжило только 38% пассажиров (342 человека из 891), в то время как погибло 62%. В таких условиях метрика Accuracy может быть сильно завышена даже при бесполезной модели. 

# ROC-AUC позволяет оценивать качество ранжирования вероятностей выживания независимо от выбранного порога и хорошо работает при дисбалансе классов. F1-score дополнительно учитывает баланс между precision и recall, что важно в задаче безопасности, где цена ошибки (непредсказанный выживший) высока.


