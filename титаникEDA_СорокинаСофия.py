# Анализ матрицы корреляций Пирсона между числовыми признаками позволяет выявить следующие важные закономерности:

# Survived имеет умеренную отрицательную корреляцию с Pclass (r = -0.34), где чем выше класс билета, тем выше шанс выживания.
# Fare показывает умеренную положительную корреляцию с Survived (r = 0.26).
# Возраст (Age) слабо отрицательно коррелирует с выживаемостью (r = -0.08).
# Между SibSp и Parch наблюдается умеренная положительная корреляция (r = 0.41).
# Pclass сильно отрицательно коррелирует с Fare (r = -0.55).
# PassengerId практически не коррелирует с другими признаками (r = 0).


# Выводы по результатам EDA

# Проведённый EDA показал, что выживаемость пассажиров «Титаника» в значительной степени определяется социально-демографическими факторами. Наиболее сильное влияние на целевую переменную Survived оказывают пол (Sex) и класс билета (Pclass). Женщины имели существенно более высокий шанс выживания по сравнению с мужчинами. Пассажиры первого класса выживали значительно чаще, чем пассажиры третьего класса.

# Стоимость билета (Fare) также положительно связана с вероятностью выживания (r = 0.26), что согласуется с влиянием Pclass (r = -0.34 с Survived). 
# Возраст демонстрирует слабую отрицательную корреляцию (r = -0.08), однако дети младшего возраста имели заметно более высокий шанс выжить.

# Датасет характеризуется умеренным дисбалансом классов (выжило ≈ 38 % пассажиров) и содержит значительное количество пропусков, особенно в признаке Cabin (687 пропусков). 
# Признаки PassengerId, Name, Ticket и Cabin имеют низкую прогностическую ценность.

# Семейные характеристики SibSp и Parch умеренно коррелируют между собой (r = 0.41), что обосновывает создание производных признаков FamilySize и IsAlone.

# Таким образом, ключевыми предикторами выживаемости являются пол, социально-экономический статус и возраст. Полученные результаты подтверждают целесообразность выбранной метрики ROC-AUC и указывают на перспективные направления дальнейшей предобработки данных (заполнение пропусков, feature engineering и кодирование категориальных переменных).